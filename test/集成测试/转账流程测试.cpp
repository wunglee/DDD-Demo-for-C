#include "gtest/gtest.h"
#include "转账流程测试环境.h"
#include "../公共测试方法.h"

void 初始化资金帐户(){
    事件发布订阅服务::构建单例()->订阅("用户已创建", "资金事件处理器");
    创建用户(用户HTTP客户端::获取单例(),"XX","YY");
}
TEST_F(转账流程测试环境, 创建用户以后创建资金帐户){
    初始化资金帐户();
    资金帐户服务接口* 资金帐户服务_= 命名服务管理器::获取单例()->查找服务<资金帐户服务接口 *>("资金帐户服务").value();
    boost::optional<资金帐户> 资金帐户1=资金帐户服务_->获取资金帐户("XX");
    boost::optional<资金帐户> 资金帐户2=资金帐户服务_->获取资金帐户("YY");
    ASSERT_EQ(资金帐户1.has_value(),true);
    ASSERT_EQ(资金帐户2.has_value(),true);
    ASSERT_EQ(资金帐户1.value().获取账号(),"XX");
    ASSERT_EQ(资金帐户2.value().获取账号(),"YY");
}
void 设置禁止收付款(bool 禁止付款, bool 禁止收款){
    资金帐户服务接口* 资金帐户服务_= 命名服务管理器::获取单例()->查找服务<资金帐户服务接口 *>("资金帐户服务").value();
    if (禁止付款) {
        资金帐户服务_->设置禁止付款("XX",true);
    }
    if (禁止收款) {
        资金帐户服务_->设置禁止收款("YY",true);
    }
}
int 创建转账(float 付款帐户资金,float 付款金额) {
    资金帐户服务接口* 资金帐户服务_= 命名服务管理器::获取单例()->查找服务<资金帐户服务接口 *>("资金帐户服务").value();
    资金帐户服务_->初始化资金("XX", 付款帐户资金);
    转账服务接口* 转账服务_= 转账服务::获取单例();
    return 转账服务_->申请转账("XX", "YY", 付款金额);
}
